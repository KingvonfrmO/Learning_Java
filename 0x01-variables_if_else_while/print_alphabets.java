public class print_alphabets {
    public static void main(String[] args) {
        char ch;
        for (ch = 'a'; ch <= 'z'; ch++){
            System.out.print(ch);
        }
        for (ch = 'A'; ch <= 'Z'; ch++){
            System.out.print(ch);
        }
        System.out.println();
    }
}
