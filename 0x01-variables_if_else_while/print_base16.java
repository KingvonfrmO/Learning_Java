public class print_base16 {
    public static void main(String[] args) {
        char ch;
        int i;

        for (i = 0; i < 10; i++){
            System.out.print(i);
        }
        for (ch = 'a'; ch <= 'f'; ch++){
            System.out.print(ch);
        }
        System.out.println();
    }
}
